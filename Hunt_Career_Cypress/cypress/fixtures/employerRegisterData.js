const createValidUser = (description, emailType) => {
    const password = generateRandomPassword();
    return {
        description,
        name: getRandomCompanyName(),
        email: generateEmail(emailType),
        password,
        confirmPassword: password,
        expectedSuccess: "getSuccessCreationMessage",
    };
};

